    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:
        if (Integer.parseInt(jTextFieldId.getText()) < 5) {
            JOptionPane.showMessageDialog(null, "Enter your ID number at least 4 disits");

        } else if (jTextFieldName.getText().trim().length() < 4) {
            JOptionPane.showMessageDialog(null, "Enter your name at 3 characters");

        } else if (Integer.parseInt(jTextFieldAge.getText()) < 20 || Integer.parseInt(jTextFieldAge.getText()) > 100) {
            JOptionPane.showMessageDialog(null, "Enter your age between 20 & 100");

        } else if (!checkEmailValidity(jTextFieldEmail.getText()) || jTextFieldEmail.getText().length() < 1) {
            JOptionPane.showMessageDialog(null, "Enter your valid email");

        } else if (jRadioButtonMale.isSelected() == false && jRadioButtonFemale.isSelected() == false) {
            JOptionPane.showMessageDialog(null, "Select your gender");

        } else if (!jCheckBoxHTML.isSelected() && !jCheckBoxJava.isSelected() && !jCheckBoxSwing.isSelected()) {
            JOptionPane.showMessageDialog(null, "Select your round");

        } else if (jComboBoxRound.getItemAt(jComboBoxRound.getSelectedIndex()).trim().equalsIgnoreCase("Select your round")) {
            JOptionPane.showMessageDialog(null, "Select your round");

        } else if (jTextAreaComments.getText().trim().length() < 10) {
            JOptionPane.showMessageDialog(null, "Enter at least 10 charecters");

        } else {
            int id = Integer.parseInt(jTextFieldId.getText());
            String name = jTextFieldName.getText();
            int age = Integer.parseInt(jTextFieldAge.getText());
            String email = jTextFieldEmail.getText();
            String gender = " ";
            if (jRadioButtonMale.isSelected()) {
                gender = jRadioButtonMale.getText();
            }
            if (jRadioButtonFemale.isSelected()) {
                gender = jRadioButtonFemale.getText();
            }
            String course = " ";
            if (jCheckBoxHTML.isSelected()) {
                course += jCheckBoxHTML.getText();
            }
            if (jCheckBoxJava.isSelected()) {
                course += jCheckBoxJava.getText();
            }
            if (jCheckBoxSwing.isSelected()) {
                course += jCheckBoxSwing.getText();
            }
            String round = jComboBoxRound.getItemAt(jComboBoxRound.getSelectedIndex());
            String comments = jTextAreaComments.getText();

            String result = id + "\n" + name + "\n" + age + "\n" + email + "\n" + gender + "\n" + course + " \n" + round + "\n" + comments;
            System.out.println(result);

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            Object[] row = new Object[8];
            row[0] = id;
            row[1] = name;
            row[2] = age;
            row[3] = email;
            row[4] = gender;
            row[5] = course;
            row[6] = round;
            row[7] = comments;
            model.addRow(row);

            Student s = new Student(id, name, age, email, gender, course, round, comments);
            List<Student> list = new ArrayList<>();
            list.add(s);
            try {
                Utils.writeToFile("Ali", list);
            } catch (Exception e) {
                Object ex = null;
                Logger.getLogger(FormValidation.class.getName()).log(Level.SEVERE, null, ex);
            }

            jLabel10.setText("Successfull added into table and write to file");

        }

    }//GEN-LAST:event_jButtonAddActionPerformed

